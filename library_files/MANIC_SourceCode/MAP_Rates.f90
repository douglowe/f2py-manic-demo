		if(present(k0h).and.present(d_soln))then
			k_cc = k0h * exp(d_soln*(1d0/tp - 1d0/tp_0))
			k_cc = k_cc * kh_conv * UgcR * tp
		elseif(present(k0h))then
			k_cc = k0h * kh_conv * UgcR * tp
		endif
	    
	    
	    ! calculate gas-aerosol mass transfer
		!dcg_dt_schwartz = -w_l*k_t*(c_g - ak*c_gv)
