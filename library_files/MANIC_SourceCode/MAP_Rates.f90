	    	acc = (a0/(1d0-a0)) * exp(d_obs*(1d0/tp - 1d0/tp_0))
	    	acc = acc / (1d0 + acc)
	    else
	    	acc = a0
	    end if
	    ! current thinking is that a value of 1 is as good a guess
	    ! as anything else -> so save computing time
	    !acc = 1d0
	
	    ! calculate the mean molecular speed (cm s^-1)
