  subroutine Update_MODEL_TRAJ_DATA
  ! Update various physical parameters which may vary during the 
  ! model trajectory. Do this using a simple linear interpolation
  ! between the data points adajcent to the current model time.
  	use physical_parameters
  
    integer, save :: prev=1
    integer, save :: next=1
	integer :: tstart, tend, order
	integer :: in
	real(kind=dp) :: scale
	
  
    ! check to see if the model time lies outside the saved data points
    if(time_traj(prev).gt.time.or.time_traj(next).lt.time)then
  		! set the range of data points to search for fit
  		if(time_traj(prev).gt.time)then
  			tstart = prev
  			tend   = 1
  			order  = -1
  		elseif(time_traj(next).lt.time)then
  			tstart = next
  			tend   = traj_length
  			order  = 1
  		endif
  		
  		! check to see if time is outside the trajectory time range
  		if( (real(order)*time_traj(tend)).lt.(real(order)*time) )then
  			! use the values at the end of the trajectory - no extrapolating!
  			prev = tend
  			next = tend
  		else ! if not then continue with the search
  			! search through the trajectory for adjacent data points
	search:	do in = tstart, tend, order
  				if( (real(order)*time_traj(in)).lt.(real(order)*time) .and. &
  						& (real(order)*time_traj(in+order)).gt.(real(order)*time) )then
  					if(order.eq.1)then
  						prev = in 
  						next = in+1
  					elseif(order.eq.-1)then
  						prev = in-1
  						next = in
  					endif
  					exit search
  				elseif( time_traj(in).eq.time )then
  					prev = in
  					next = in
  					exit search
  				endif
  			end do search !in = tstart, tend, order
  		endif
	endif

	! now calculate the trajectory data (if prev=next no need to interpolate)
	if(prev.eq.next)then
		temp  = temp_traj(prev)
		pres  = pres_traj(prev)
		z_mbl = zmbl_traj(prev)
		tidal_height  = tide_traj(prev)
		SUN   = sun_traj(prev)
		do in = 1, traj_species
			C(traj_no(in)) = species_traj(in,prev)
		end do ! in=1,traj_species
	else
		! calculate 0-1 scaling factor based on time position
		scale = (time-time_traj(prev))/(time_traj(next)-time_traj(prev))
		! interpolate the data
		temp  = temp_traj(prev) + scale*(temp_traj(next)-temp_traj(prev))
		pres  = pres_traj(prev) + scale*(pres_traj(next)-pres_traj(prev))
		z_mbl = zmbl_traj(prev) + scale*(zmbl_traj(next)-zmbl_traj(prev))
		tidal_height = tide_traj(prev) + scale*(tide_traj(next)-tide_traj(prev))
		SUN   = sun_traj(prev) + scale*(sun_traj(next)-sun_traj(prev))
		do in = 1, traj_species
			C(traj_no(in)) = species_traj(in,prev) + &
								scale*(species_traj(in,next)-species_traj(in,prev))
		end do ! in=1,traj_species
    endif


	! update aerosol deposition and emission velocities. For the moment
	! just use a reduced fixed value for all particles, and set the emission
	! velocity to be the same as the deposition velocity.	
	v_d(:,1) = 1.653439153e-6
	v_d(:,2) = 5.787037037e-6
	
	v_e = v_d


  end subroutine Update_MODEL_TRAJ_DATA




    real(kind=dp) function het_rct(a0,d_obs,tp,rad,radc,Mmass,w_l, &
    			num,fx,cH2O,cClm,cBrm)


	! Calculate the gas-aerosol mass transfer (dc_g/dt)
	! according to the formulation of Schwartz (1986)
	! (taken from von Glasow et al, 2002)
	! het_react = -k_t*w_l*fun/Het_t (molec cm_air^3 s^-1)
	! where:
	! k_t = mass transfer coefficient
	!   k_t = [r^2/(3*D_g) + (4*r)/3*v*a ]^(-1)
	!   r = particle radius (m)
	!   D_g = gas phase diffusion 
	!     D_g=lv/3; l=mean free path (m)
	!   v = mean molecular speed
	!     v = sqrt(8*R*T/(M*pi))
	!       R = universal gas constant (use UgcR)
	!       T = temperature
	!       M = molar mass
	!   a = accomodation coefficient (dimensionless)
	! fx/Het_t = a function of the H2O/Cl-/Br- content 
	!		of the aerosol used to work out the 
	!		proportions of the reactions
	!    fx = [H2O]  or  f(Cl-)[Cl-]  or  f(Br-)[Br-]
	!    Het_t = [H2O] + f(Cl-)[Cl-] + f(Cl-)[Br-]
	! w_l = dimensionless liquid water content (m_aq^3/cm_air^3)
	! 	(equivalent to the total aerosol volume,
	!	e.g. Sander, Surveys in Geophysics, 1999)
	! note: must use either c_gv *or* c_a & k0h (&d_soln)
	use physical_parameters
	real(kind=dp), optional :: a0
	real(kind=dp), optional :: d_obs
	real(kind=dp) :: tp, rad, Mmass, w_l, radc
	real(kind=dp), optional :: fx, cH2O, cClm, cBrm
	real(kind=dp) :: acc, k_t, num
	
	! collision diameter of gas molecule (m)
	real(kind=dp), parameter :: d_col = 1d-10
	! zero point temperature (K)
	real(kind=dp), parameter :: tp_0 = 298d0
	! mean free path (m)
	real(kind=dp) :: lambda
	! mean molecular speed (m s^-1)
	real(kind=dp) :: v_bar
	! molecular diffusion coeff (m^2 s^-1)
	real(kind=dp) :: d_c


	if(num.ne.0d0)then
	    ! calculate mean free path (m)
	    lambda = (mm_air/(mm_air+Mmass))**0.5d0
	    ! convert C(ind_M) from molec cm-3 to molec m-3 (x1d6) 
	    lambda = 1d0 / (pi*d_col**2*(C(ind_M)*1d6)) * lambda 

	    ! calculate accomodation coefficient (dimensionless)
	    !if(present(d_obs)) then
	    !	acc = (a0/(1d0-a0)) * exp(d_obs*(1d0/tp - 1d0/tp_0))
	    !	acc = acc / (1d0 + acc)
	    !else
	    !	acc = a0
	    !end if
	    ! current thinking is that a value of 1 is as good a guess
	    ! as anything else -> so save computing time
	    acc = 1d0

	    ! calculate the mean molecular speed (cm s^-1)
	    v_bar = (8d0*UgcR*tp/(Mmass*pi))**(0.5d0)
	    ! convert to m s^-1 from cm s^-1
	    v_bar = v_bar/mtocm

	    ! calculate mass transfer coefficient (s^-1)
	    k_t = radc**2d0/(lambda*v_bar)
	    k_t = (k_t + 4d0*rad/(3d0*v_bar*acc))**(-1d0)

	    
	    if(present(fx)) then
	    	het_rct = k_t*w_l*fx/(cH2O + fClm*cClm + fBrm*cBrm)
	    else
	    	het_rct = k_t*w_l
	    endif 

	else   ! if no particles then no hetero reactions
 	    het_rct = 0d0
	endif
 
    end function het_rct




