	!          c_a = aqueous-phase concentration (mol m_aq^-3)
	!          k_cc = dimensionless Henry constant
	!                 k_cc = k_h * R * T
	!                 k_h = Henry's law coefficient
	!                 (k_h = k0h*f(d_soln))
	! w_l = dimensionless liquid water content (m_aq^3/cm_air^3)
	! 	(equivalent to the total aerosol volume,
	!	e.g. Sander, Surveys in Geophysics, 1999)
	use physical_parameters
	real(kind=dp), optional :: k0h, d_soln, c_a
	real(kind=dp), optional :: c_gv
