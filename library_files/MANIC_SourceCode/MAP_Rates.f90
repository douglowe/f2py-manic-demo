	    ! calculate mass transfer coefficient (s^-1)
	    k_t = radc**2d0/(lambda*v_bar)
	    k_t = (k_t + 4d0*rad/(3d0*v_bar*acc))**(-1d0)

	    ! calculate molecular diffusion coeff (m^2 s^-1)
	    !d_c = 5d0*pi*(mm_air+Mmass)/(64d0*Mmass)
	    !d_c = d_c * v_bar * lambda
	
